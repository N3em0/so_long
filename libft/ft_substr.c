#include "libft.h"

char	*ft_substr(const char *s, unsigned int start, size_t len)
{
	size_t	i;
	size_t	j;
	char	*sub;

	i = 0;
	j = 0;
	if (!s)
		return (NULL);
	while (s[j] && j != start)
		j++;
	if (len > ft_strlen((char *)s) - j)
		len = ft_strlen((char *)s) - j;
	sub = ft_calloc(1, (len + 1));
	if (!sub)
		return (NULL);
	while (i < len && s[i + start])
	{
		sub[i] = s[i + start];
		i++;
	}
	sub[i] = '\0';
	return (sub);
}
